export const getCrateById = (id) => crates.find(c => c.id === id);
export const getCrateBySlug = (slug) => crates.find(c => c.slug === slug);
export const getCratesByAge = (ageGroup) => crates.filter(c => {
  if (ageGroup === '0-2') return c.ageRange === '0-2';
  if (ageGroup === '2-4') return c.ageRange === '2-4';
  if (ageGroup === '5-8') return c.ageRange === '5-8';
  if (ageGroup === '9-12') return ['9-16+', '6-11'].includes(c.ageRange);
  if (ageGroup === '12+') return ['14-104', '9-16+'].includes(c.ageRange);
  return true;
});
